import createRouter from "./base.js";

import {
  getUser,
  getUsers,
  getEmail,
  createUser,
  updateUser,
  deleteUser,
} from "../../controllers/v1/user.js";

import {
  validatePostUser,
  validatePutUser,
} from "../../middleware/validation.js";

const userController = {
  get: getUsers,
  getById: getUser,
  getByEmail: getEmail,
  create: createUser,
  update: updateUser,
  delete: deleteUser,
};

const userRouter = createRouter(
  userController,
  validatePostUser,
  validatePutUser
);

export default userRouter;

